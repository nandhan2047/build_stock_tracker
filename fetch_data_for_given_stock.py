def clean_value(value_str):
    """
    Cleans a string value by removing non-numeric characters (except for '-' and '.') and converts to float.
    Handles 'None', '-' or 'inf' values gracefully.
    """
    if isinstance(value_str, (int, float)):
        return value_str
    if not isinstance(value_str, str):
        return None

    value_str = value_str.strip().replace(',', '') # Remove commas
    if value_str.lower() in ('none', '-', 'inf', 'n/a'):
        return None

    # Handle 'T', 'B', 'M' suffixes
    multiplier = 1
    if value_str.endswith('T'):
        multiplier = 1e12
        value_str = value_str[:-1]
    elif value_str.endswith('B'):
        multiplier = 1e9
        value_str = value_str[:-1]
    elif value_str.endswith('M'):
        multiplier = 1e6
        value_str = value_str[:-1]

    # Remove any remaining non-numeric chars except for '.' and '-'
    cleaned_text = re.sub(r'[^-.0-9]', '', value_str)

    if cleaned_text and cleaned_text != '.' and cleaned_text.count('.') <= 1 and cleaned_text != '-':
        try:
            return float(cleaned_text) * multiplier
        except ValueError:
            return None
    return None


