def scrape_finviz_country_data(country_code: str, output_dir: str = '.', max_pages: int = 20, ticker_symbol: Union[str, List[str], None] = None) -> pd.DataFrame:
    """
    Scrapes stock data from Finviz for a specified country and a given number of pages,
    saves it to a CSV, and returns a DataFrame.

    Args:
        country_code (str): The country code to filter Finviz data (e.g., 'USA').
        output_dir (str): The directory where the CSV file will be saved. Defaults to current directory.
        max_pages (int): The maximum number of pages to scrape.
        ticker_symbol (Union[str, List[str], None], optional): A specific stock ticker symbol or list of symbols to filter the results by. Defaults to None.

    Returns:
        pd.DataFrame: A DataFrame containing the scraped stock data.
    """
    base_url = "https://finviz.com/screener.ashx"
    headers_request = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'
    }

    all_scraped_data = []
    column_headers = []

    # Set base_params to filter by the country_code
    base_params = {
        'c': country_code
    }

    # Logic to process ticker_symbol for server-side filtering
    tickers_to_filter = []
    if ticker_symbol is not None:
        if isinstance(ticker_symbol, str):
            tickers_to_filter.append(ticker_symbol.upper())
        elif isinstance(ticker_symbol, list):
            for ticker in ticker_symbol:
                if isinstance(ticker, str):
                    tickers_to_filter.append(ticker.upper())

    if tickers_to_filter:
        ticker_filter_string = ','.join(tickers_to_filter)
        base_params['t'] = ticker_filter_string

    for page_num in range(1, max_pages + 1):
        offset = (page_num - 1) * 20 + 1
        current_params = base_params.copy()
        current_params['p'] = offset

        print(f"Scraping page {page_num} of {max_pages} for {country_code} with offset {offset}...")

        try:
            response = requests.get(base_url, params=current_params, headers=headers_request)
            response.raise_for_status() # Raise an exception for bad status codes
            time.sleep(5) # Add a delay to avoid being blocked
        except requests.exceptions.RequestException as e:
            print(f"Request failed for page {page_num}: {e}")
            break # Stop if a request fails

        soup = BeautifulSoup(response.text, 'html.parser')

        table = soup.find('table', class_='screener_table')
        if not table:
            table = soup.find('table', {'width': '100%', 'cellpadding': '3', 'cellspacing': '0'}) # Fallback

        if not table:
            print(f"Could not find the screener results table on page {page_num}. Ending scraping.")
            break
        else:
            # Extract column headers only once from the first page
            if not column_headers:
                header_row = table.find('tr')
                if header_row:
                    column_headers = [th.get_text(strip=True) for th in header_row.find_all('th')]
                else:
                    print(f"Could not find header row on page {page_num}.")
                    break

            # Extract data rows
            page_data = []
            all_rows = table.find_all('tr')

            # Determine the starting index for data rows, skipping the header if found
            start_row_index = 0
            if column_headers and all_rows and all_rows[0].find('th'):
                start_row_index = 1

            for row in all_rows[start_row_index:]:
                cells = [td.get_text(strip=True) for td in row.find_all('td')]
                # Ensure cells are not empty and match header length if headers exist
                if cells and (not column_headers or len(cells) == len(column_headers)):
                    page_data.append(cells)

            if page_data:
                all_scraped_data.extend(page_data)
            else:
                print(f"No new data rows found on page {page_num}. Ending scraping.")
                break

    if all_scraped_data and column_headers:
        df = pd.DataFrame(all_scraped_data, columns=column_headers)
    else:
        print("No data or headers were successfully scraped.")
        df = pd.DataFrame(columns=column_headers if column_headers else [])

    # The original client-side filtering logic for ticker_symbol is now redundant if server-side filtering is used.
    # However, for robustness (e.g., if Finviz changes its URL parameter or for multiple tickers not fully supported by 't'),
    # keeping it as a fallback or for additional filtering is an option. For this subtask, we assume 't' is sufficient.
    # The previous filtering was: df_filtered_by_ticker = df[df['Ticker'].str.upper() == ticker_symbol.upper()].copy()
    # Since the 't' parameter handles this server-side, this block is removed.

    # Save DataFrame to CSV
    if not df.empty:
        os.makedirs(output_dir, exist_ok=True)
        # Adjust file name if specific tickers were filtered via the 't' parameter
        if tickers_to_filter:
            file_name = f"finviz_{country_code}_stocks.csv"
        else:
            file_name = f"finviz_{country_code}_stocks.csv"
        file_path = os.path.join(output_dir, file_name)
        df.to_csv(file_path, index=False)
        print(f"DataFrame saved to: {os.path.abspath(file_path)}")
    else:
        print(f"No data to save for {country_code} with specified tickers.")

    return df

