def get_yahoo_finance_page_with_session(ticker, url_path):
    """
    Attempts to fetch a Yahoo Finance page, handling the consent page using a persistent session.
    Returns a BeautifulSoup object of the page if successful, None otherwise.
    """
    session = requests.Session() # Initialize a session to persist cookies
    base_url = "https://finance.yahoo.com/quote/"
    target_url = f"{base_url}{ticker}{url_path}"

    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
        'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8',
        'Accept-Language': 'en-US,en;q=0.9',
        'Accept-Encoding': 'gzip, deflate, br',
        'Connection': 'keep-alive',
        'Upgrade-Insecure-Requests': '1',
        'Sec-Fetch-Dest': 'document',
        'Sec-Fetch-Mode': 'navigate',
        'Sec-Fetch-Site': 'none',
        'Sec-Fetch-User': '?1'
    }

    print(f"Attempting initial GET request to: {target_url} using session...")
    try:
        response_initial = session.get(target_url, headers=headers, allow_redirects=True)
        response_initial.raise_for_status()
        time.sleep(3)

        if "consent" in response_initial.url.lower() or "consent.cmp.oath.com" in response_initial.url.lower() or "consent.yahoo.com" in response_initial.url.lower() or "We need your consent" in response_initial.text:
            print(f"Detected Yahoo consent page at: {response_initial.url}")

            soup_consent = BeautifulSoup(response_initial.text, 'html.parser')
            consent_form = soup_consent.find('form', {'action': True, 'method': 'post'})
            if not consent_form:
                print("Could not find the consent form on the page. Cannot bypass.")
                return None

            form_data = {}
            for input_tag in consent_form.find_all('input', {'type': 'hidden'}):
                name = input_tag.get('name')
                value = input_tag.get('value')
                if name and value:
                    form_data[name] = value

            post_url = consent_form.get('action')
            if not post_url.startswith('http'):
                from urllib.parse import urljoin
                post_url = urljoin(response_initial.url, post_url)

            form_data['agree'] = 'agree'
            form_data['.submit'] = 'agree'
            headers['Referer'] = response_initial.url

            print(f"Submitting consent POST request to: {post_url} using session...")
            response_post = session.post(post_url, data=form_data, headers=headers, allow_redirects=True)
            response_post.raise_for_status()
            time.sleep(5)

            print(f"Consent POST successful. Final URL after consent: {response_post.url}")
            return BeautifulSoup(response_post.text, 'html.parser')
        else:
            print(f"Successfully accessed target page: {response_initial.url}")
            return BeautifulSoup(response_initial.text, 'html.parser')

    except requests.exceptions.RequestException as e:
        print(f"Error during Yahoo Finance access for {ticker}: {e}")
        return None
    except Exception as e:
        print(f"An unexpected error occurred during Yahoo Finance access: {e}")
        return None

def _scrape_single_ticker_insider_transactions_data(ticker):
    """
    Helper function to scrape insider transactions data for a single ticker from Yahoo Finance.
    """
    soup = get_yahoo_finance_page_with_session(ticker, '/insider-transactions/')
    if not soup:
        return pd.DataFrame()

    target_table = soup.find('table', class_='yf-idy1mk')
    if not target_table:
        print(f"Could not find the insider transactions table (table.yf-idy1mk) for {ticker}.")
        return pd.DataFrame()

    print(f"Extracting data from insider transactions table for {ticker}...")

    headers = []
    header_row = target_table.find('thead').find('tr')
    if header_row:
        headers = [th.get_text(strip=True) for th in header_row.find_all('th')]

    if not headers:
        print(f"Could not extract headers from the insider transactions table for {ticker}.")
        return pd.DataFrame()

    data = []
    for row in target_table.find('tbody').find_all('tr'):
        cells = [td.get_text(strip=True) for td in row.find_all('td')]
        if cells and len(cells) == len(headers):
            data.append(cells)

    if data:
        df = pd.DataFrame(data, columns=headers)
        df['Ticker'] = ticker

        # Apply clean_value_yahoo to relevant numerical columns
        for col in ['Shares', 'Value', 'Shares Total', 'Price']:
            if col in df.columns:
                df[col] = df[col].apply(clean_value_yahoo)

        return df
    else:
        print(f"No data rows found in the insider transactions table for {ticker}.")
        return pd.DataFrame(columns=headers + ['Ticker'])

