def clean_value_yahoo(value_str):
    if isinstance(value_str, (int, float)):
        return value_str
    if not isinstance(value_str, str):
        return None

    value_str = value_str.strip().replace(',', '').replace('$', '')

    if value_str.lower() in ('none', '-', 'inf', 'n/a'):
        return None

    multiplier = 1
    if value_str.endswith('B'):
        multiplier = 1e9
        value_str = value_str[:-1]
    elif value_str.endswith('M'):
        multiplier = 1e6
        value_str = value_str[:-1]
    elif value_str.endswith('K'):
        multiplier = 1e3
        value_str = value_str[:-1]
    elif value_str.endswith('%'):
        multiplier = 0.01
        value_str = value_str[:-1]

    cleaned_text = re.sub(r'[^-.0-9]', '', value_str)

    if cleaned_text and cleaned_text != '.' and cleaned_text.count('.') <= 1 and cleaned_text != '-':
        try:
            return float(cleaned_text) * multiplier
        except ValueError:
            return None
    return None

def scrape_dataroma_investor_data(ticker):
    """
    Scrapes super investor holdings and insider trading summary data for a given ticker
    from Dataroma.com.

    Args:
        ticker (str): The stock ticker symbol (e.g., 'AAPL').

    Returns:
        tuple: A tuple containing two pandas DataFrames:
               - super_investor_df (pd.DataFrame): Data about super investor holdings for the ticker.
               - insider_trades_df (pd.DataFrame): Data about insider trades summary for the ticker.
               Returns empty DataFrames if data cannot be retrieved or parsed.
    """
    dataroma_base_url = "https://www.dataroma.com/m/stock.php"
    search_url = f"{dataroma_base_url}?sym={ticker}"
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'
    }

    super_investor_data = []
    insider_trades_data = []
    super_investor_columns = []
    insider_trades_columns = []

    try:
        response = requests.get(search_url, headers=headers)
        response.raise_for_status() # Raise an HTTPError for bad responses (4xx or 5xx)
        time.sleep(3) # Be polite and add a delay

        soup = BeautifulSoup(response.text, 'html.parser')

        # --- Extract Super Investor Holdings from table with id="grid" ---
        super_investor_table = soup.find('table', id='grid')
        if super_investor_table:
            # print(f"[DEBUG] Found Superinvestor (id='grid') table for {ticker}.")
            header_row = super_investor_table.find('thead').find_all('tr')[0]
            super_investor_columns = [th.get_text(strip=True) for th in header_row.find_all(['th', 'td'])]
            if super_investor_columns and super_investor_columns[0] == '':
                super_investor_columns = super_investor_columns[1:]

            data_rows = super_investor_table.find('tbody').find_all('tr')
            for row in data_rows:
                cells = [td.get_text(strip=True) for td in row.find_all('td')]
                if cells and cells[0] == '\u2261':
                    cells = cells[1:]

                if cells and len(cells) == len(super_investor_columns):
                    super_investor_data.append(cells)

        # --- Extract Insider Buying/Selling Summary from table with id="ins_sum" ---
        insider_trades_table = soup.find('table', id='ins_sum')
        if insider_trades_table:
            # print(f"[DEBUG] Found Insider Trades (id='ins_sum') table for {ticker}.")
            all_theads_in_ins_sum = insider_trades_table.find_all('thead')
            if len(all_theads_in_ins_sum) >= 2:
                header_row = all_theads_in_ins_sum[1].find('tr')
                if header_row:
                    insider_trades_columns = [th.get_text(strip=True) for th in header_row.find_all(['th', 'td'])]

                data_rows = insider_trades_table.find('tbody').find_all('tr')
                for row in data_rows:
                    cells = [td.get_text(strip=True) for td in row.find_all('td')]
                    if cells and len(cells) == len(insider_trades_columns):
                        insider_trades_data.append(cells)

    except requests.exceptions.RequestException as e:
        print(f"Error fetching data for {ticker} from Dataroma: {e}")
    except Exception as e:
        print(f"An unexpected error occurred while parsing data for {ticker}: {e}")

    # Create DataFrames. Ensure columns are set only if data exists and matches column count.
    super_investor_df = pd.DataFrame(super_investor_data, columns=super_investor_columns if super_investor_data else [])
    insider_trades_df = pd.DataFrame(insider_trades_data, columns=insider_trades_columns if insider_trades_data else [])

    if not super_investor_df.empty:
        super_investor_df['Ticker'] = ticker
    if not insider_trades_df.empty:
        insider_trades_df['Ticker'] = ticker

    return super_investor_df, insider_trades_df

